/**
 * Convert Java string to Rust-compatible C string
 * Caller must free returned string with free()
 */
static char* jstring_to_cstr(JNIEnv* env, jstring jstr) {
    if (!jstr) return NULL;

    const char* cstr = (*env)->GetStringUTFChars(env, jstr, NULL);
    if (!cstr) return NULL;

    size_t len = strlen(cstr);
    char* result = (char*)malloc(len + 1);
    if (result) {
        memcpy(result, cstr, len + 1);
    }

    (*env)->ReleaseStringUTFChars(env, jstr, cstr);
    return result;
}

/**
 * Convert Rust result to Java exception
 */
static void throw_runtime_exception(JNIEnv* env, const char* message) {
    jclass exCls = (*env)->FindClass(env, "java/lang/RuntimeException");
    if (exCls) {
        (*env)->ThrowNew(env, exCls, message);
    }
}

